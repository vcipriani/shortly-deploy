var mongoose = require('mongoose');
var crypto = require('crypto');
var bcrypt = require('bcrypt-nodejs');
var Promise = require('bluebird');

mongoose.connect('localhost', 'shortly');

exports.User = mongoose.Schema({
  username: {type: 'String', unique: true},
  password: {type: 'String'}  
}, {timestamps: true});

exports.User.methods.comparePassword = function(attemptedPassword, callback) {
  bcrypt.compare(attemptedPassword, this.password, function(err, isMatch) {
    callback(isMatch);
  });
};

exports.User.pre('save', function(next) {
  var cipher = Promise.promisify(bcrypt.hash);
  cipher(this.password, null, null).bind(this)
    .then(function(hash) {
      this.password = hash;
      next();
    });
});

exports.Link = mongoose.Schema({
  url: {type:'String'},
  baseUrl: {type:'String'},
  code: {type:'String'},
  title: {type:'String'},
  visits: {type:'Number'}
}, {timestamps: true});

exports.Link.pre('save', function(next) {
  var shasum = crypto.createHash('sha1');
  shasum.update(this.url);
  this.code = shasum.digest('hex').slice(0, 5);
  next();
});


